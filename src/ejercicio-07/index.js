// Ejercicio 7 - Reemplazar Subcadena

// Dada una cadena de texto, utiliza el método replace() para reemplazar una subcadena específica por otra. Luego, imprime la cadena resultante.
const frase = 'El perro es fiel y el gato es independiente'
const fraseReemplazada = frase.replace('perro','pájaro')

console.log(fraseReemplazada)
// Resultado: 'El pájaro es fiel y el gato es independiente'
